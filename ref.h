    /**
     * @brief Writes a value to a field in a register.
     * @param blk (token) The name of the block that contains @p [reg].
     * @param reg (token) The name of the register that contains @p [fld].
     * @param fld (token) The name of the field to write to.
     * @param val (integer expression) The value to write.
     */
    #define _WRITE(blk, reg, fld, val) \
        (_##blk##_##reg = (_##blk##_##reg & ~_##blk##_##reg##_##fld) | \
        (((uintmax_t)(val) << _##blk##_##reg##_##fld##_POS) & _##blk##_##reg##_##fld))

    /**
     * @brief Sets the bits of a field in a register.
     * @param blk (token) The name of the block that contains @p [reg].
     * @param reg (token) The name of the register that contains @p [fld].
     * @param fld (token) The name of the field to set.
     */
    #define _SET(blk, reg, fld) \
        (_##blk##_##reg |= _##blk##_##reg##_##fld)

    /**
     * @brief Clears the bits of a field in a register.
     * @param blk (token) The name of the block that contains @p [reg].
     * @param reg (token) The name of the register that contains @p [fld].
     * @param fld (token) The name of the field to clear.
     */
    #define _CLR(blk, reg, fld) \
        (_##blk##_##reg &= ~_##blk##_##reg##_##fld)

    /**
     * @brief Reads the value of a field in a register.
     * @param blk (token) The name of the block that contains @p [reg].
     * @param reg (token) The name of the register that contains @p [fld].
     * @param fld (token) The name of the field to read.
     * @returns (integer expression) The value of the field.
     */
    #define _READ(blk, reg, fld) \
        ((_##blk##_##reg & _##blk##_##reg##_##fld) >> _##blk##_##reg##_##fld##_POS)

    /**
     * @brief Determines if the bits of a field in a register are set.
     * @param blk (token) The name of the block that contains @p [reg].
     * @param reg (token) The name of the register that contains @p [fld].
     * @param fld (token) The name of the field to query.
     * @returns (boolean expression) True if al the bits in the field are set,
     *          or false otherwise.
     */
    #define _IS_SET(blk, reg, fld) \
        ((_##blk##_##reg & _##blk##_##reg##_##fld) == _##blk##_##reg##_##fld)

    /**
     * @brief Determines if the bits of a field in a register are cleared.
     * @param blk (token) The name of the block that contains @p [reg].
     * @param reg (token) The name of the register that contains @p [fld].
     * @param fld (token) The name of the field to query.
     * @returns (boolean expression) True if all the bits in the field are cleared,
     *          or false otherwise.
     */
    #define _IS_CLR(blk, reg, fld) \
        ((_##blk##_##reg & _##blk##_##reg##_##fld) == 0U)       

